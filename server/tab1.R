data_sets <- c("mtcars", "diamonds")

myData <- reactive({
        if(input$smooth == TRUE){
            data <- switch(input$example_ds,
                           "mtcars" = mtcars) 
            data
        }
        else{
            inFile <- input$file1
            if (is.null(inFile)) return(NULL)
            data <- read.csv(inFile$datapath, header=input$header, sep=input$sep, 
                             quote=input$quote)
            data   
        }

})
output$contents <- renderTable({
    myData()
})